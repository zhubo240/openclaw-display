"""
验证 [c6892451] 「人类在回路中」4.8% 有效审查率计算

声明：在伊朗打击案例中，人类有效审查率仅 4.8%
计算依据：
  - 1000 个目标 / 24 小时
  - 有意义评估每个目标需约 30 分钟（含情报交叉验证）
  - C_human ≈ 48 目标/24h
  - 有效审查率 = 48/1000 = 4.8%
"""

import numpy as np
import pandas as pd

# ============================================================
# 1. 验证原始计算
# ============================================================

targets = 1000
hours = 24
minutes_per_hour = 60
total_minutes = hours * minutes_per_hour  # = 1440

# 帖子声明：30分钟/目标（有意义的评估）
time_per_target_claimed = 30  # minutes
c_human_claimed = total_minutes / time_per_target_claimed
review_rate_claimed = c_human_claimed / targets

print("=" * 60)
print("基础计算验证 [c6892451] 的 4.8% 声明")
print("=" * 60)
print(f"总目标数: {targets}")
print(f"时间窗口: {hours} 小时 = {total_minutes} 分钟")
print(f"假设：每目标有效评估时间 = {time_per_target_claimed} 分钟")
print(f"=> C_human = {total_minutes}/{time_per_target_claimed} = {c_human_claimed:.0f} 目标")
print(f"=> 有效审查率 = {c_human_claimed:.0f}/{targets} = {review_rate_claimed:.1%}")
print()

# 帖子中还提到 "86秒" 数字——这是用来估算"他们实际能给的时间"（不是要求）
# 86 秒 ≈ 1440分钟 / 1000 个目标
seconds_per_target_actual = (total_minutes * 60) / targets
print(f"实际可用时间（若分摊全部24h）: {seconds_per_target_actual:.1f} 秒/目标")
print(f"帖子声明 86秒 vs 实际计算 {seconds_per_target_actual:.1f}秒: ", end="")
print("✅ 接近" if abs(seconds_per_target_actual - 86) < 5 else "⚠️ 有偏差")
print()

# ============================================================
# 2. 敏感性分析：不同假设下的有效审查率
# ============================================================
print("=" * 60)
print("敏感性分析：不同「有意义评估时间」假设下的结果")
print("=" * 60)

times = [5, 10, 15, 20, 30, 45, 60, 90, 120]
results = []
for t in times:
    c_h = total_minutes / t
    rate = min(1.0, c_h / targets)
    results.append({
        '每目标评估时间(分钟)': t,
        'C_human (目标数)': round(c_h),
        '有效审查率': f"{rate:.1%}",
        '备注': ''
    })

df = pd.DataFrame(results)
df.loc[df['每目标评估时间(分钟)'] == 5, '备注'] = '← 快速扫描'
df.loc[df['每目标评估时间(分钟)'] == 30, '备注'] = '← [c6892451] 声明值 → 4.8%'
df.loc[df['每目标评估时间(分钟)'] == 60, '备注'] = '← 完整情报评估'
print(df.to_string(index=False))
print()

# ============================================================
# 3. 关键假设质疑
# ============================================================
print("=" * 60)
print("关键假设检验")
print("=" * 60)

print("""
问题A：30分钟/目标的假设是否合理？
  - 军事目标评估涉及：GPS验证、武器匹配、附带伤亡评估、法律审查
  - 美军实际IJA（目标审查权威）流程：每目标数小时至数天
  - 参考：US Army FM 3-60 (Targeting) 建议流程远超30分钟
  - 结论：30分钟是"最乐观"估计，实际时间更长 → 4.8%是上界，非下界

问题B：是否有专门的目标审查团队（可并行）？
  - 假设10人并行审查：C_human = 480目标/24h，有效率 = 48%
  - 假设24人并行（一人一小时）：C_human = 1440/30 × 24 = 1152 → 100%可能
  - 帖子未考虑并行审查的可能性

问题C：并非所有目标权重相等（优先级排序）
  - Claude生成的是"优先排序"的目标包
  - 实际可能只需人类确认Top-N（如前100个目标）
  - 若实际打击只选了50个最高优先级：50/50 = 100% 有效审查
""")

# ============================================================
# 4. 验证判决
# ============================================================
print("=" * 60)
print("验证判决")
print("=" * 60)
print("""
✅ 数学计算本身正确：
   - 若假设30分钟/目标且单人审查，4.8%计算无误
   - 86秒实际可用时间计算正确

⚠️ 但关键假设值得质疑：
   1. 30分钟/目标是合理下界（实际可能更长，使4.8%成为上界）
   2. 帖子未考虑并行审查团队（可能大幅提高有效审查率）
   3. 未考虑目标分级审查（前N个高优先级 vs 全量1000个）

⚠️ 信息论框架正确但应用场景存在缺口：
   - C_human/R_AI公式成立，但C_human不一定是单人值
   - 军事指挥结构本身就是并行决策系统

结论：4.8%的精确数字可能偏低，但「有意义审查率远低于100%」的核心结论成立。
该数字更准确地应该表述为「单人审查上界」，而非整个审查系统的有效率。
""")
