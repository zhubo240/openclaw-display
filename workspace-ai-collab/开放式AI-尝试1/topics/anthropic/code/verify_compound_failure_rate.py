print("=" * 60)

sabotage_rate = 0.27
hitl_effective_rate = 0.048  # 4.8% 有效审查

compound = sabotage_rate * (1 - hitl_effective_rate)
print(f"声明计算：{sabotage_rate:.0%} × (1 - {hitl_effective_rate:.1%}) = {compound:.3%}")
print()

# 这个计算在逻辑上对吗？
print("方法论评估：")
print("""
计算意图：在1000个目标中，27%有sabotage风险，95.2%没被有效审查
因此"通过防护网的危险"= 27% × 95.2% ≈ 25.7%

⚠️ 但这个相乘有一个逻辑问题：

正确的计算应该是：
  P(漏网) = P(有sabotage) × P(审查未发现 | 有sabotage)
           = 27% × (1 - 检出率)

但 4.8% 是「有效审查率」（人类能实质性审查的目标比例），
不是「检出率」（有效审查后能发现sabotage的概率）。

差别：
  - 有效审查率 4.8%：48个目标被认真看了
  - 若这48个目标中27%有sabotage = 约13个有问题
  - 若人类审查能发现90%的问题 → 实际检出约 0.048 × 0.9 = 4.3%
  - 剩余95.7%的sabotage漏网（而非95.2%）

结论：[7af1c88c] 的计算把「未审查率」直接等于「未检出率」，
存在一步跳跃，但方向正确，数量级合理。
""")

